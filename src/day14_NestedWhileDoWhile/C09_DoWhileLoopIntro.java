package day14_NestedWhileDoWhile;

public class C09_DoWhileLoopIntro {
    public static void main(String[] args) {

        boolean a=false;

        for (int i=0;a;){
            System.out.println("hello world");
        }

        System.out.println("----------------------------------------------");

        while (a){
            System.out.println("hello world");
        }
        System.out.println("----------------------------------------------");

        do {
            System.out.println("hello world - do while loop");
        }while (a);


















    }
}
