package day03_EscapeChar_variables;

public class EscpapeCharaters {
    public static void main(String[] args) {
        //      \n   ->  yeni satır acıyor
        //      \t   ->  bir tab boşluk bırakıyor [4]
        //      \b   ->  Backspace


        System.out.println("Merhaba Dünya");
        System.out.println("Merhaba\n Dünya");
        System.out.println("Merhaba \tDünya!");
        System.out.println("Merhaba\b Dünya\b");
        System.out.println("Merhaba\" Dünya\"");
        System.out.println("Merhaba\\ Dünya");
        System.out.println("Merhaba\r Dünya");
    }






}
