package day10_UnaryLogicalOperAndReview;

public class Tasks1 {
    public static void main (String[] args){

        int x=10;
        --x;
        System.out.println(x);
        ++x;
        System.out.println(x);

        x--;
        System.out.println(x);
        x++;
        System.out.println(x);
    }
}
